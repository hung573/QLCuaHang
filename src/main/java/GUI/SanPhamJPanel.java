    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            SanPham sanPham = new SanPham();
            sanPham = this.sanphamUpdate;
            sanPham.setTenSanPham(jtfTenSanPham.getText());
            sanPham.setIdLoaiSanPham(Integer.parseInt(jtfIdLoaiSanPham.getText()));
            sanPham.setGia(Integer.parseInt(jtfGia.getText()));
            sanPham.setSoLuong(Integer.parseInt(jtfSoLuong.getText()));
            sanPham.setMoTa(jTextArea.getText());
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String finddate =  sdf.format(jdtNgaySanXuat.getDate()).toString();
            sanPham.setNgaySanXuat( Date.valueOf(finddate));
            sanPham.setHinhAnh(jlbTenAnh.getText());

            if(JOptionPane.showConfirmDialog(this, "Bạn có muốn Update không?", "Hỏi"
                , JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.NO_OPTION){
            return;
        }
        SanPhamBUS sanPhamBUS = new SanPhamBUS();
        if(sanPhamBUS.updateSanPham(sanPham) > 0){
            Files.copy(Paths.get(jlbDuongDanAnh.getText()), Paths.get(duongdandata + jlbTenAnh.getText()),StandardCopyOption.REPLACE_EXISTING);
            JOptionPane.showMessageDialog(null, "Update thành công");
            jtfTenSanPham.setText("");
            jtfIdLoaiSanPham.setText("");
            jdtNgaySanXuat.cleanup();
            jtfGia.setText("");
            jtfSoLuong.setText("");
            jTextArea.setText("");
            loadData();
        }

        else{
            JOptionPane.showMessageDialog(null, "Update thất bại");
        }
        } catch (Exception e) {
            Logger.getLogger(SanPhamJFrame.class.getName()).log(Level.SEVERE, null, e);
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

