    private void jtbSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbSanPhamMouseClicked
        try {
            int row = jtbSanPham.getSelectedRow();
            if(row >= 0){
                int id = (Integer) jtbSanPham.getValueAt(row, 1);
                SanPhamBUS sanphamBUS = new SanPhamBUS();
                SanPham sp = sanphamBUS.getSanPham(id);
                sanphamUpdate = this.sanphamBUS.getSanPham(id);
                sanphamDelete = this.sanphamBUS.getSanPham(id);
                if(sp != null){
                    jtfTenSanPham.setText(sp.getTenSanPham());
                    jtfIdLoaiSanPham.setText(String.valueOf(sp.getIdLoaiSanPham()));
                    jtfGia.setText(DecimalFormat.format(sp.getGia()));
                    jtfSoLuong.setText(String.valueOf(sp.getSoLuong()));
                    lbimage.setIcon(ResizeImage(String.valueOf(duongdandata+sp.getHinhAnh())));
                    System.out.println(duongdandata+sp.getHinhAnh());
                    jdtNgaySanXuat.setDate(sp.getNgaySanXuat());
                    jTextArea.setText(sp.getHinhAnh());
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jtbSanPhamMouseClicked

